export type Permit = { name: string; agency: string; basis: string; flag?: string; note?: string };

// 스팟 → 필요한 허가 목록
export function requiredPermits(s: Spot): Permit[] {
  const list: Permit[] = [];

  // 1) 하천 점용허가 (모든 하천 공통)
  list.push({
    name: "하천 점용허가 (공작물 신설)",
    agency: s.authority,
    basis: "하천법 제33조 · 「하천점용허가 세부기준」",
    flag: s.grade === "국가하천" ? "처분청이 시·구 아님(복잡)" : "구청 단일창구",
  });

  // 2) 보호구역 관련 — 천연기념물 현상변경
  if (s.zone === "안") {
    list.push({
      name: "국가유산(천연기념물) 현상변경 허가",
      agency: "국가유산청",
      basis: "자연유산법 제17조 (천연기념물 제179호 보호구역)",
      flag: "선결",
      note: "공유수면 내 시설도 원칙적으로 허가 대상. 1단계 입지로는 부적합.",
    });
    list.push({
      name: "습지보전법 협의",
      agency: "낙동강유역환경청(환경부)",
      basis: "습지보전법 제13조 (습지보호지역 행위제한)",
    });
    list.push({
      name: "공유수면 점용·사용허가",
      agency: "공유수면 관리청(부산시/구)",
      basis: "공유수면 관리 및 매립에 관한 법률 제8조",
    });
  } else if (s.zone === "확인필요") {
    list.push({
      name: "보호구역 포함 여부 사전 확인",
      agency: "국가유산청 · " + s.region + "청",
      basis: "자연유산법 제17조 / 고시 지형도면 대조",
      flag: "조건부",
      note: "하구 말단·외곽 500m 보존지역 저촉 시 현상변경 허가가 추가될 수 있음.",
    });
  }

  // 3) 환경영향 (소규모, 해당 시)
  list.push({
    name: "소규모 환경영향 검토 (해당 시)",
    agency: s.region + "청 / 유역환경청",
    basis: "환경영향평가법 — 대상·규모 해당 시에 한함",
    flag: "해당시",
  });

  return list;
}

// 스팟 → 협의·진행 순서
export function pathway(s: Spot): string[] {
  const steps: string[] = [];
  steps.push(`${s.region}청 하천 담당부서 사전 상담 — 처분청·구비서류·표준 처리기간 확인`);
  if (s.zone !== "밖") {
    steps.push("국가유산청·" + s.region + "청에 보호구역/보존지역 경계 사전 확인(고시 지형도면 대조)");
  }
  steps.push("정보공개청구 — 하천구역 도면·하천기본계획 적합성·하폭 자료 확보");
  steps.push("간이 현장조사 — 계류점 수충부·연약지반·통수단면 검토");
  steps.push("규제 신속확인 또는 사전 협의 — 필요한 허가를 공식 확인");
  steps.push("하천 점용허가 신청 — 사업계획서·도면 첨부");
  if (s.zone === "안") steps.push("국가유산청 현상변경 허가 및 습지·공유수면 허가 병행");
  steps.push("거치·운영 — 주 1회 수거, 영상·수위 기록, 수위 단계 철거 SOP");
  return steps;
}

