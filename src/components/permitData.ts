export type Permit = { name: string; agency: string; basis: string; flag?: string; note?: string };

// 하천 법정 등급 → 점용허가 근거·처분청 분기 (붐·CCTV 공통).
// ★지방하천/국가하천=하천법 제33조, 소하천=소하천정비법 제14조, 기타하천=법정 미지정(확인 필요).
export function streamBasis(s: Spot): { basis: string; agency: string; flag?: string; note?: string } {
  switch (s.grade) {
    case "소하천":
      return {
        basis: "소하천정비법 제14조 (소하천의 점용 등)",
        agency: `${s.region}청장 (소하천관리청)`,
        flag: "구청 단일창구",
        note: "신청서=소하천정비법 시행규칙 별지 제11호서식. 처리기간 20일, 그 안에 통지 없으면 허가 의제. 국가·지방하천으로 유입되는 소하천은 관리청이 하천관리청에 통지하고 환경부에 보고. 표준 첨부=지반조사위치도·지반조사방법·원상복구계획서 등(비굴착·가역이면 지반조사 완화).",
      };
    case "기타하천":
      return {
        basis: "법정 미지정(기타하천) — 점용 근거 확인 필요",
        agency: `${s.region}청 (구거·공유재산·조례 확인)`,
        flag: "확인필요",
        note: "하천법(법정하천)·소하천정비법(소하천) 어디에도 미지정. 복개·구거 여부와 점용 근거(공유재산법·지자체 조례)를 관할 구청에 먼저 확인.",
      };
    case "국가하천":
      return {
        basis: "하천법 제33조 · 「하천점용허가 세부기준」",
        agency: s.authority,
        flag: "처분청이 시·구 아님(복잡)",
      };
    default: // 지방하천
      return {
        basis: "하천법 제33조 · 「하천점용허가 세부기준」",
        agency: s.authority,
        flag: "구청 단일창구",
      };
  }
}

// 스팟 → 필요한 허가 목록 (use=boom: 붐 실증, use=cctv: 환경감시 CCTV)
export function requiredPermits(s: Spot, use: UseType = "boom"): Permit[] {
  const list: Permit[] = [];
  const sb = streamBasis(s);
  const streamWord = s.grade === "소하천" ? "소하천" : "하천";

  // 1) 점용허가 (등급별 근거 분기)
  list.push({
    name: use === "cctv"
      ? `${streamWord} 점용허가 (CCTV 지주·관로 설치)`
      : `${streamWord} 점용허가 (부유식 붐 공작물)`,
    agency: sb.agency,
    basis: sb.basis,
    flag: sb.flag,
    note: sb.note,
  });

  // 1-2) CCTV 전용 — 영상정보처리기기 설치·운영(개인정보 보호법)
  if (use === "cctv") {
    list.push({
      name: "영상정보처리기기 설치·운영 (행정예고·운영방침·안내판)",
      agency: `${s.region}청 협의 (공동 운영·활용 기관)`,
      basis: "개인정보 보호법 제25조",
      flag: "주민 의견수렴",
      note: "공개된 장소의 CCTV는 설치 전 20일 이상 행정예고, 운영·관리방침 공개, 안내판 게시가 필요. 지자체 공동명의가 깔끔.",
    });
    list.push({
      name: "민관 데이터 협약(MOU) 또는 기부채납",
      agency: `${s.region}청 하천관리과·정보통신과`,
      basis: "협약(MOU) 또는 공유재산 및 물품 관리법(기부채납)",
      flag: "사업방식 결정",
      note: "방식 B(MOU 운영)는 데이터 주도권, 방식 A(기부채납)는 운영자 책임 이전. 권고는 실증 MOU → 지속 기부채납 하이브리드.",
    });
  }

  // 2) 보호구역 관련 — 천연기념물 현상변경
  if (s.zone === "안") {
    list.push({
      name: "국가유산(천연기념물) 현상변경 허가",
      agency: "국가유산청",
      basis: "자연유산법 제17조 (천연기념물 제179호 보호구역)",
      flag: "선결",
      note: "공유수면·하천 내 시설도 원칙적으로 허가 대상. 1단계 입지로는 부적합.",
    });
    list.push({
      name: "습지보전법 협의",
      agency: "낙동강유역환경청(환경부)",
      basis: "습지보전법 제13조 (습지보호지역 행위제한)",
    });
    list.push({
      name: "공유수면 점용·사용허가",
      agency: "공유수면 관리청(부산시/구)",
      basis: "공유수면 관리 및 매립에 관한 법률 제8조",
    });
  } else if (s.zone === "확인필요") {
    list.push({
      name: "보호구역 포함 여부 사전 확인",
      agency: "국가유산청 · " + s.region + "청",
      basis: "자연유산법 제17조 / 고시 지형도면 대조",
      flag: "조건부",
      note: "하구 말단·외곽 500m 보존지역 저촉 시 현상변경 허가가 추가될 수 있음.",
    });
  }

  // 3) 환경영향 (소규모, 해당 시)
  list.push({
    name: "소규모 환경영향 검토 (해당 시)",
    agency: s.region + "청 / 유역환경청",
    basis: "환경영향평가법 — 대상·규모 해당 시에 한함",
    flag: "해당시",
  });

  return list;
}

// 스팟 → 협의·진행 순서
export function pathway(s: Spot, use: UseType = "boom"): string[] {
  const steps: string[] = [];
  steps.push(`${s.region}청 하천 담당부서 사전 상담 — 처분청·구비서류·표준 처리기간 확인`);
  if (s.grade === "기타하천") {
    steps.push(`${s.region}청에 ${s.stream} 복개·구거 여부와 점용 근거(공유재산·조례) 확인`);
  }
  if (use === "cctv") {
    steps.push(`${s.region}청 하천관리과·환경과·정보통신과 합동 미팅 제안 — 칸막이 돌파`);
    steps.push("민관 데이터 협약(MOU) 또는 기부채납 조건 협의 — 운영주체·데이터 공유항목 확정");
  }
  if (s.zone !== "밖") {
    steps.push("국가유산청·" + s.region + "청에 보호구역/보존지역 경계 사전 확인(고시 지형도면 대조)");
  }
  steps.push("정보공개청구 — 하천구역 도면·하천기본계획 적합성·하폭 자료 확보");
  if (use === "cctv") {
    steps.push("개인정보 보호법 제25조 행정예고 20일 이상 게시(지자체 공동명의) + 운영·관리방침 수립");
    steps.push("점용허가 신청(지주 위치도·촬영범위·비식별 설계 첨부)");
  } else {
    steps.push("간이 현장조사 — 계류점 수충부·연약지반·통수단면 검토");
    steps.push("규제 신속확인 또는 사전 협의 — 필요한 허가를 공식 확인");
    steps.push("하천 점용허가 신청 — 사업계획서·도면 첨부");
  }
  if (s.zone === "안") steps.push("국가유산청 현상변경 허가 및 습지·공유수면 허가 병행");
  if (use === "cctv") {
    steps.push("설치·연계 — 엣지 비식별화 적용, 환경데이터 추출, 이타시티·지자체 공유 개시");
  } else {
    steps.push("거치·운영 — 주 1회 수거, 영상·수위 기록, 수위 단계 철거 SOP");
  }
  return steps;
}

